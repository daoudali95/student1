package com.company;

public class Student3 {
private String name;
private int age;
private String uni_name;
private int roll_n;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getUni_name() {
        return uni_name;
    }

    public void setUni_name(String uni_name) {
        this.uni_name = uni_name;
    }

    public int getRoll_n() {
        return roll_n;
    }

    public void setRoll_n(int roll_n) {
        this.roll_n = roll_n;
    }
}

